// Определение языка системы
const getSystemLanguage = (): Language => {
  const systemLang = navigator.language.toLowerCase();
  
  if (systemLang.startsWith('ru')) {
    return 'ru';
  } else if (systemLang.startsWith('uk')) {
    return 'uk';
  } else {
    return 'en';
  }
};

